void	method_1_4_3_x_x_3(int *arr)
{
	if (arr[0] == 1 && arr[1] == 4 && arr[2] == 3 && arr[5] == 3)
	{
		arr[3] = 1;
		arr[4] = 2;
	}
	if (arr[0] == 3 && arr[3] == 3 && arr[4] == 4 && arr[5] == 1)
	{
		arr[1] = 2;
		arr[2] = 1;
	}
}
