int	is_print(char c)
{
	return (c >= 32 && c <= 126);
}

int	ft_str_is_printable(char *str)
{
	int	i;

	i = 1;
	while (*str)
	{
		if (!is_print(*str))
			i = 0;
		str++;
	}
	return (i);
}
