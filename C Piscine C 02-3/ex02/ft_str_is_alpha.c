int	is_alpha(char c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
}

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 1;
	while (*str)
	{
		if (!is_alpha(*str))
			i = 0;
		str++;
	}
	return (i);
}
