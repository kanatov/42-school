int	is_numeric(char c)
{
	return (c >= '0' && c <= '9');
}

int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 1;
	while (*str)
	{
		if (!is_numeric(*str))
			i = 0;
		str++;
	}
	return (i);
}
