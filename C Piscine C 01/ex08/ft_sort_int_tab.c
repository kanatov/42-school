void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	tmp;

	i = 1;
	while (i < size)
	{
		if (tab[i - 1] > tab[i])
		{
			tmp = tab[i - 1];
			tab[i - 1] = tab[i];
			tab[i] = tmp;
			i--;
			if (i < 1)
				i = 1;
		}
		else
		{
			i++;
		}
	}
}
