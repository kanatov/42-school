int	ft_is_prime(int nb)
{
	int	i;
	int	cap;

	if (nb < 2)
		return (0);
	i = 2;
	cap = nb / 2;
	while (i <= cap)
	{
		if (!(nb % i))
			return (0);
		i++;
	}
	return (1);
}
