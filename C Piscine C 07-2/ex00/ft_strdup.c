#include <stdlib.h>

char	*ft_strdup(char *s)
{
	int		i;
	char	*str;

	i = 0;
	while (s[i])
		i++;
	i++;
	str = (char *)malloc(sizeof(char) * i);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
